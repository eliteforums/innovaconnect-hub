-- ============================================================
-- RESYNC FUNCTION: rebuild total_referrals from scratch
-- Run this if the cached counts ever drift from live data:
--   SELECT resync_referral_counts();
-- ============================================================
CREATE OR REPLACE FUNCTION resync_referral_counts()
RETURNS void AS $$
BEGIN
  UPDATE community_partners cp
  SET
    total_referrals = (
      SELECT COUNT(*) FROM registrations r WHERE r.ref_code = cp.ref_code
    ),
    updated_at = NOW();
END;
$$ LANGUAGE plpgsql;
